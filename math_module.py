import math

number = int(input("Enter a number: "))
square_root = math.sqrt(number)
logarithm = math.log(number)
sine = math.sin(number)

print(f"The square root of a {number} is {square_root}")
print(f"The logarithm of {number} is {logarithm}")
print(f"The sine value of {number} is {sine}")
